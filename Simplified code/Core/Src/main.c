int main(void)
{
  HAL_Init();

  EE_Init(EE_FORCED_ERASE);

  SystemClock_Config();

  MX_GPIO_Init();
  MX_DMA_Init();
  MX_SPI1_Init();
  MX_USART2_UART_Init();
  MX_ADC1_Init();
  MX_CRC_Init();

  Initialisation();

  char *ptab = tab;
  uint8_t isAlreadyStopped = 1;

  int adc = 0;
  HAL_ADC_Start_DMA(&hadc1, &adc, 1);

  while (1)
  {
	  if(adc < 50)
	  {
		  if(isAlreadyStopped == 0)
		  		  {
		  			  //SoftStop
		  	      uint8_t msg1[1] = {0xb0};
		  	      SPI_TransmitWithDelay(&hspi1, msg1, 1, 1000, byte_delay);
		  	      HAL_Delay(2000);
		  	      //HardHiZ
		  	      uint8_t msg2[1] = {0xa8};
		  	      SPI_TransmitWithDelay(&hspi1, msg2, 1, 1000, byte_delay);
		  	      isAlreadyStopped = 1;

		  	      ptab = tab;
		  		  }
		  else
		  {
			  if(HAL_UART_Receive(&huart2, (uint8_t *)ptab, 1, 1000) == HAL_OK)
        {
		      if (*ptab == '\r')
		      {
		        *ptab = '\0';
		        Commands(tab);
		        ptab = tab;
		        }
		        else
		        {
		        ptab++;
		        if (ptab >= tab + sizeof(tab))
		        {
		          ptab = tab;
		        }
		      }
			  }
		  }
	  }
	  else
	  {
		  HAL_Delay(100);
		  Run(forward, adc*coef*0.01);
		  isAlreadyStopped = 0;
	  }
  }
}

